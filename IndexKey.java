/**
 * gives index value to letter.
 *
 * @version 1.0
 */
public class IndexKey
{
   public int charToIndex(char let)
    {
    int index = 0;
    if(let == 'a' || let == 'A')
    {
        index = 0;
    }
     if(let == 'b' || let == 'B')
    {
        index = 1;
    }
    if(let == 'C' || let == 'c')
    {
        index = 2;
    }
     if(let == 'd' || let == 'D')
    {
        index = 3;
    }
    if(let == 'e' || let == 'E')
    {
        index = 4;
    }
     if(let == 'f' || let == 'F')
    {
        index = 5;
    }
    if(let == 'g' || let == 'G')
    {
        index = 6;
    }
     if(let == 'H' || let == 'h')
    {
        index = 7;
    }
    if(let == 'I' || let == 'i')
    {
        index = 8;
    }
     if(let == 'j' || let == 'J')
    {
        index = 9;
    }
    if(let == 'k' || let == 'K')
    {
        index = 10;
    }
     if(let == 'l' || let == 'L')
    {
        index = 11;
    }
    if(let == 'M' || let == 'm')
    {
        index = 12;
    }
     if(let == 'n' || let == 'N')
    {
        index = 13;
    }
    if(let == 'o' || let == 'O')
    {
        index = 14;
    }
     if(let == 'P' || let == 'p')
    {
        index = 15;
    }
    if(let == 'Q' || let == 'q')
    {
        index = 16;
    }
     if(let == 'R' || let == 'r')
    {
        index = 17;
    }
    if(let == 'S' || let == 's')
    {
        index = 18;
    }
     if(let == 'T' || let == 't')
    {
        index = 19;
    }
    if(let == 'U' || let == 'u')
    {
        index = 20;
    }
     if(let == 'V' || let == 'v')
    {
        index = 21;
    }
    if(let == 'W' || let == 'w')
    {
        index = 22;
    }
     if(let == 'x' || let == 'X')
    {
        index = 23;
    }
    if(let == 'Y' || let == 'y')
    {
        index = 24;
    }
     if(let == 'z' || let == 'Z')
    {
        index = 25;
    }
    return index;
    }
}
